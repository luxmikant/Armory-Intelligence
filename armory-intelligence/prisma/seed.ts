async function main() {
  console.log("ðŸ”« Seeding Armory Intelligence database...\n");

  // Clear existing data
  console.log("Clearing existing data...");
  await prisma.firearm.deleteMany();
  await prisma.safetyChecklist.deleteMany();
  await prisma.historicalEra.deleteMany();

  // Seed firearms
  console.log(`\nSeeding ${firearms.length} firearms...`);
  for (const firearm of firearms) {
    await prisma.firearm.create({ data: firearm });
    process.stdout.write(".");
  }
  console.log(" Done!");

  // Seed safety checklists
  console.log(`\nSeeding ${safetyChecklists.length} safety checklists...`);
  for (const checklist of safetyChecklists) {
    await prisma.safetyChecklist.create({ data: checklist });
    process.stdout.write(".");
  }
  console.log(" Done!");

  // Seed historical eras
  console.log(`\nSeeding ${historicalEras.length} historical eras...`);
  for (const era of historicalEras) {
    await prisma.historicalEra.create({ data: era });
    process.stdout.write(".");
  }
  console.log(" Done!");

  console.log("\nâœ… Database seeded successfully!");
  console.log(`   - ${firearms.length} firearms`);
  console.log(`   - ${safetyChecklists.length} safety checklists`);
  console.log(`   - ${historicalEras.length} historical eras`);
}

main()
  .catch((e) => {
    console.error("âŒ Seed error:", e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
